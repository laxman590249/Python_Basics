# Binary Number
for i in range(1, 20):
    print(f"Binary of {i:>2} is : {i:>08b}")

# Octal Numbers
for i in range(1, 20):
    print(f"Octal of {i:>2} is : {i:>08o}")

# Hexa Decimals
for i in range(1, 20):
    print(f"Hexa of {i:>2} is : {i:>08x}")

print(f"{10:04b}")
